var evenOrOdd = function(num){

if (num%2==0) {
                return "Even";
                    
} else {
                return "Odd";
}

}
var result=evenOrOdd(45);
console.log(`45 number is ${result}`);

var result=evenOrOdd(70);
console.log(`70 number is ${result}`);

var result=evenOrOdd(67);
console.log(`67 number is ${result}`);

